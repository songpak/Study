import math

print(math.ceil(5.1)) #n값 이상의 수 중 가장 작은수를 반환
print(math.ceil(5.999))
print(math.ceil(-4.88))

print(math.floor(3.5)) #n값 이하의 수 중 가장 큰 수를 반환
print(math.floor(3.2))
print(math.floor(-4.2))

print(math.fabs(-20)) #내장함수 abs와 동일

print(math.pow(2,4)) #내장함수 pow와 동일

print(math.pi) #원주율

print(math.sqrt(100)) #제곱근

print(math.trunc(100.2322)) #소수점 이하 잘라서 버린값 반환

